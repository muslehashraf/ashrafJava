package escape_sequences;

public class SpecialCharacters {
    public static void main(String[] args){

        /*
        apple\\\orange

        I Like "sundays" and apple

         My fav fruits are "kiwi and "Orange"

         */

        System.out.println("apple\\\\\\orange");
        System.out.println("I Like \"Sunday\" and apple");
        System.out.println("My fav fruits are \napple, \n\"kiwi\" \nand \"Orange\"");
    }
}
