package loops.fori_loops;

public class Exercise01_PrintNumberAscending {
    public static void main(String[] args) {

        /*
        Write a Java program to print numbers from 1 to 10
        (1 and 10 are included)
        Expected output:
        0
        1
        2
        .
        .
        .
        8
        9
        10

         */

        for(int i = 1; i <= 10; i++){
            System.out.println(i);
        }

    }
}
